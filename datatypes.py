# Data types in python

# Number
num = 10
print(type(num), num)

# Float
b = 10.98
print(type(b), b)

# String
name = "Nisha Singh"
print(type(name), name)

# Boolean
isSingle = True
print(type(isSingle), isSingle)

# List in python
l = ["banana", "oranges", 20, False, True, 3435.3]
print(type(l))
# empty list
l1=[]
print(l1, type(l1))

# Tuple in Python
t = ("banana", "oranges", 20, False, True, 3435.3)
print(type(t))
# empty tuple
t1=()
print(t1, type(t1))

# Dictionaries in python
d = {
  "name":"Nisha",
  "age":24,
  "isSingle":True
}
print(type(d))
# empty dictionary 
d1 = {}
print(d1, type(d1))

# Set is python
s = {4,2,6,2,7,5,8,4}
print(s, type(s))
# empty set
s1 = set()
print(s1, type(s1))
